library(shiny)
library(plotly)

# Define UI for application that draws a histogram
shinyUI(fluidPage(

    # Application title
    titlePanel("Old Faithful Geyser Data"),

    # Sidebar with a slider input for number of bins
    sidebarLayout(
        sidebarPanel(
            selectInput(
                inputId='var_name',
                label='Variable',
                choices=c("HOPITBED", "EMPLPSYS"),
                selected=1
            ),
            selectInput(
                inputId='unit_name',
                label='Unit Name',
                choices=c("RTOINPNB", "PERSMYNB"),
                selected=1
            ),
            sliderInput("bins",
                        "Number of bins:",
                        min = 1,
                        max = 50,
                        value = 30)
        ),

        # Show a plot of the generated distribution
        mainPanel(
            plotOutput("distPlot"),
            plotlyOutput("spaghetti_1")
        )
    )
))
